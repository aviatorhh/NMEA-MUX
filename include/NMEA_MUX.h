/**
 * Calculate CRC from EEPROM
 * @param index The beginning of the calculation
 */
uint16_t eeprom_crc() {
    uint16_t crc = 0xffff;

    for (uint16_t pos = 2; pos < 1024; pos++) {
        crc ^=
            (uint16_t)eeprom_read_byte((uint8_t*)pos);  // XOR byte into least sig. byte of crc

        for (int i = 8; i != 0; i--) {  // Loop over each bit
            if ((crc & 0x0001) != 0) {  // If the LSB is set
                crc >>= 1;              // Shift right and XOR 0xA001
                crc ^= 0xA001;
            } else          // Else LSB is not set
                crc >>= 1;  // Just shift right
        }
    }
    // Note, this number has low and high bytes swapped, so use it accordingly (or
    // swap bytes)
    return crc;
}

/**
 * Calculate free memory
 */
int freeMemory() {
    char top;
#ifdef __arm__
    return &top - reinterpret_cast<char*>(sbrk(0));
#elif defined(CORE_TEENSY) || (ARDUINO > 103 && ARDUINO != 151)
    return &top - __brkval;
#else   // __arm__
    return __brkval ? &top - __brkval : &top - __malloc_heap_start;
#endif  // __arm__
}

